    private void preencherComboProdutos() {
        ProdutosDAO produtosDAO = new ProdutosDAO();
        List<EgProdutos> listaProdutos = produtosDAO.listar();
        for (EgProdutos produto : listaProdutos) {
            jCboProdutos.addItem(produto);
        }
    }

    public EgVendaProdutos viewBean() {
        EgVendaProdutos vendaProdutos = new EgVendaProdutos();
        vendaProdutos.setEgProdutos((EgProdutos) jCboProdutos.getSelectedItem());
        vendaProdutos.setQuantidade(Util.strToInt(jTxtQuantidade.getText()));
        vendaProdutos.setValorUnitario(Util.strToDuble(jTxtValorUnitario.getText()));
        vendaProdutos.setValorTotal(vendaProdutos.getQuantidade() * vendaProdutos.getValorUnitario());
        return vendaProdutos;
    }

    public void beanView(EgVendaProdutos vendaProdutos) {
        jCboProdutos.setSelectedItem(vendaProdutos.getEgProdutos());
        jTxtQuantidade.setText(String.valueOf(vendaProdutos.getQuantidade()));
        jTxtValorUnitario.setText(Util.doubleToStr(vendaProdutos.getValorUnitario()));
        jTxtTotal.setText(Util.doubleToStr(vendaProdutos.getQuantidade() * vendaProdutos.getValorUnitario()));
    }

